package dto;

import lombok.ToString;

@ToString
public class Post {

	//모델링 한거다.  이런 클래스를 dto 라고 한다
	public int userId;
	public int id;
	public String title;
	public String body;
	public int getUserId() {
		return userId;
	}
	public void setUserId(int userId) {
		this.userId = userId;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getBody() {
		return body;
	}
	public void setBody(String body) {
		this.body = body;
	}
	
	
}
